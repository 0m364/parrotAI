package com.oblivionburn.nlp;

class WordData
{
    private String Word;
    private Integer Frequency;

    String getWord()
    {
        return Word;
    }

    void setWord(String word)
    {
        Word = word;
    }

    Integer getFrequency()
    {
        return Frequency;
    }

    void setFrequency(Integer frequency)
    {
        Frequency = frequency;
    }
}
